def main():
    """ input 5 count """
    num = int(input())
    if num >= 4:
        print("Mo Chit (N8)")
        print("Saphan Khwai (N7)")
    if num >= 6:
        print("Ari (N5)")
        print("Sanam Pao (N4)")
    if num >= 7:
        print("Victory Monument (N3)")
    if num >= 8:
        print("Phaya Thai (N2)")
        print("Ratchathewi (N1)")
    if num >= 9:
        print("Siam (CEN)")
main()
